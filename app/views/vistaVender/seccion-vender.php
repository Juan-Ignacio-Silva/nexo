    <h1>Manual de Instrucciones para vender – Nexo </h1>

    <h2>1. Requisitos principales para vender</h2>
    <ul>
        <li><strong>Ser mayor de edad (18 años o más)</strong> – requisito <strong>FUNDAMENTAL</strong> para poder vender en Nexo.</li>
        <li>Tener una <strong>cuenta activa</strong> en la plataforma.</li>
        <li>Contar con un <strong>correo electrónico válido</strong> para recibir notificaciones.</li>
        <li>Disponer de una información clara del producto que quiera publicar:
            <ul>
                <li>Nombre del artículo</li>
                <li>Descripción detallada (estado, características, etc.)</li>
                <li>Precio del producto</li>
                <li>Fotos reales y de buena calidad<strong> (No se permiten fotos realizadas por IA)</strong></li>
            </ul>
        </li>
    </ul>

    <h2>2. Pasos para publicar un producto</h2>
    <ol>
        <li>Iniciá sesión con tu correo y contraseña.</li>
        <li>Entrá al menú y hacé clic en <strong>“Vender”</strong>.</li>
        <li>Seleccioná <strong>“Publicar producto”</strong>.</li>
        <li>Completá el formulario con:
            <ul>
                <li>Nombre del producto</li>
                <li>Categoría correspondiente. Ejemplo: Tecnologia - Celulares</li>
                <li>Precio y método de pago</li>
                <li>Imágenes claras (mínimo 1, recomendado hasta 5)</li>
                <li>Descripción detallada del producto</li>
            </ul>
        </li>
        <li>Revisá todos los datos ingresados.</li>
        <li>Hacé clic en <strong>“Publicar”</strong> para confirmar.</li>
    </ol>

    <h2>3. ¿Cómo gestionar tus publicaciones?</h2>
    <p>Podés ver, editar o eliminar tus productos desde <strong>“Mi cuenta → Mis publicaciones”</strong>.  
    Si un comprador se interesa, recibirás una notificación en la web y por correo electrónico.  
    Recordá responder al comprador y coordinar los detalles de pago y entrega.</p>

    <h2>4. Información para compradores</h2>
    <ul>
        <li>También deben ser <strong>mayores de edad</strong> y tener cuenta en la web.</li>
        <li>Pueden buscar productos, revisar fotos, precios y descripción.</li>
        <li>Contactar al vendedor mediante la plataforma para hacer preguntas.</li>
        <li>El pago y la entrega se acuerdan entre comprador y vendedor.</li>
    </ul>

    <h2>5. Recomendaciones de seguridad</h2>
    <ul>
        <li>Usar datos <strong>reales y verificables</strong> en tu cuenta.</li>
        <li>No compartir información sensible (documentos, contraseñas, etc.).</li>
        <li>Hacer entregas en lugares públicos y seguros.</li>
        <li>Guardar comprobantes de pago y mensajes dentro de la plataforma.</li>
        <li><strong>Recordar qué:</strong> Todos los usuarios deben ser mayores de edad<strong> (Obligatorio)</strong>.</li>
    </ul>
